#include <stdio.h>


int main() {

	int a[5] = { 9,1,7,3,5 }; // 정수 a를 5개의 배열로 지정 ( 9,1,7,3,5 )
	int i, j, min, t; // 정수 i,j,min,t를 값 없이 선언
	for (i = 0; i < 4; i++) { // i의 초기값을 0으로 지정하고 i가 4보다 작으면 반복문 실행. 실행 후 i 후위연산
		min = i; // min에다가 i의 값을 넣기

		for (j = i + 1; j < 5; j++) { // j의 초기값을 i + 1로 지정, j가 5보다 작으면 반복문 실행 . 실행 후 j 후위연산
			if (a[j] < a[min]) // 만약 a[j]가 a[min]보다 작다면 
				min = j; // min에다가 j의 값을 넣는다
		}

		t = a[i]; // t 에다가 a[i]의 값을 넣기 ( ex) i = 0 이면 t = 9 ) 
		a[i] = a[min]; // a[i]에다가 a[min]의 값을 넣기 ( ex) min은 i는 0이라고 가정한 후 i에 안의 반복문 j에서 반복문 연산을 마치면 min은 0이다. 그러면 a[0] = 0 )
		a[min] = t; // a[min]에다가 t의 값을 넣기 ( ex) 위 i는 0이라고 가정할때 min은 0이다. 그러므로 a[0] = 9 )
	}

	for (i = 0; i < 5; i++) { // i = 0이라고 값을 지정하고 만약 i가 5보다 작다면 반복문 실행 , 반복문 실행 후 i 후위연산
		printf("%2d", a[i]); // printf로 2byte의 최소 바이트값을 지정한 후 a[i] 출력 i가 0이라면 a[0]로 %2d 출력
	}



	// 첫번째 for i 문 >> min = i;  min = 0
	// for i 문 안의 for j 문 실행. (j = i + 1) j = 1 
	// a[min] = 9 a[j] = 1
	// if (a[j] < a[min]) >> 1 < 9
	// min = j; 실행



	// 첫번째 for i 문 >> min = i;  min = 0 (값: 9)
	// for i 문 안의 for j 문 실행. (j = i + 1) j = 1 
	// a[min] = 9, a[j] = 1
	// if (a[j] < a[min]) >> 1 < 9 
	// min = j; 실행 >> min = 1 

	// --- 이어서 ---
	// j = 2 일 때: a[min] = 1, a[j] = 7 >> 7 < 1 (거짓) -> 통과
	// j = 3 일 때: a[min] = 1, a[j] = 3 >> 3 < 1 (거짓) -> 통과
	// j = 4 일 때: a[min] = 1, a[j] = 5 >> 5 < 1 (거짓) -> 통과
	// j 반복문 종료

	// 이제 자리 바꾸기(Swap) 타임!
	// t = a[i]; >> t = a[0] (9) -> 9를 임시 보관함 t에 저장
	// a[i] = a[min]; >> a[0] = a[1] (1) -> 0번 방에 드디어 제일 작은 '1'을 넣음
	// a[min] = t; >> a[1] = 9 -> 아까 보관했던 9를 1번 방으로 보냄
	// 결과: { 1, 9, 7, 3, 5 }


	return 0;
}